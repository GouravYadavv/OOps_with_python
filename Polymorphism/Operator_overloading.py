# Operator overloading provides the ability to override the functionality of a built-in operator in user-defined classes.

"""
    So operator overloading is simply a technique to use an operator in more than one way.
    Like '+' plus sign when used between the integers or floats to performs addition between them, but when it is used between two strings 
    it combines them and print them together. 
"""

a="Hello"
b="World!"

print(a+b)

# It can also be done like
print("Hello"+"World!")

# Let's use '+' plus sign for addition.
print(2+3)

